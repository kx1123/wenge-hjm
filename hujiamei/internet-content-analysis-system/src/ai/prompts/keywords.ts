/**
 * 关键词提取提示词（区分网媒和微博）
 * 网媒：重事实（如 offer、测算、分析）
 * 微博：重情绪（如 崩溃、风景很好、心情）
 */
export function getKeywordsPrompt(type: 'webmedia' | 'weibo', content: string, title?: string): string {
  if (type === 'webmedia') {
    return `请从以下网媒文章中提取3-5个关键词。

文章标题：${title || '无'}
文章内容：${content}

要求：
1. 提取最能代表文章核心内容的关键词
2. 关键词应该是媒体报道关注的焦点（重事实、客观性）
3. 关键词要具体、准确、有意义
4. 优先提取：事件名称、数据、分析结果等事实性关键词

请严格按照JSON数组格式返回，不要包含任何其他文字说明，例如：["offer", "测算", "分析"]`
  } else {
    return `请从以下微博内容中提取3-5个关键词。

内容：${content}

要求：
1. 提取最能代表用户讨论热点的关键词
2. 关键词应该是用户关注的核心话题（重情绪、观点）
3. 关键词要具体、准确、有意义
4. 优先提取：情感表达、热门话题、用户关注点等情绪性关键词

请严格按照JSON数组格式返回，不要包含任何其他文字说明，例如：["崩溃", "风景很好", "心情"]`
  }
}

