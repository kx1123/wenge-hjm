/**
 * 舆情分类提示词
 */
export function getCategoryPrompt(
  type: 'webmedia' | 'weibo',
  content: string,
  title?: string
): string {
  if (type === 'webmedia') {
    return `请对以下网媒文章进行舆情分类。文章标题：${title || '无'}，文章内容：${content}。

请从以下类别中选择最合适的一个：
- 投诉：用户或机构对服务、产品、政策等的投诉
- 建议：对改进、优化提出的建议
- 咨询：询问信息、政策、服务等
- 表扬：对服务、产品、政策等的正面评价
- 中性报道：客观的新闻报道，无明显倾向
- 其他：不属于以上类别的其他类型

请只返回类别名称，例如：投诉、建议、咨询、表扬、中性报道、其他。`
  } else {
    return `请对以下微博内容进行舆情分类。内容：${content}。

请从以下类别中选择最合适的一个：
- 投诉：用户对服务、产品、政策等的投诉
- 建议：对改进、优化提出的建议
- 咨询：询问信息、政策、服务等
- 表扬：对服务、产品、政策等的正面评价
- 中性讨论：客观的讨论，无明显倾向
- 其他：不属于以上类别的其他类型

请只返回类别名称，例如：投诉、建议、咨询、表扬、中性讨论、其他。`
  }
}

