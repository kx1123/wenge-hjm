/**
 * 话题识别提示词
 */
export function getTopicPrompt(
  type: 'webmedia' | 'weibo',
  content: string,
  title?: string
): string {
  if (type === 'webmedia') {
    return `请从以下网媒文章中识别主要话题和议题。文章标题：${title || '无'}，文章内容：${content}。

请识别1-3个主要话题，每个话题应该是：
1. 具体明确的议题或主题
2. 能够概括文章讨论的核心内容
3. 便于后续聚类和关联分析

请严格按照JSON数组格式返回，不要包含任何其他文字说明，例如：["话题1", "话题2", "话题3"]。`
  } else {
    return `请从以下微博内容中识别主要话题。内容：${content}。

请识别1-3个主要话题，每个话题应该是：
1. 具体明确的议题或主题
2. 能够概括用户讨论的核心内容
3. 便于后续聚类和关联分析

请严格按照JSON数组格式返回，不要包含任何其他文字说明，例如：["话题1", "话题2", "话题3"]。`
  }
}

